/*
- Escreva um loop (for) que exiba a frase abaixo no console;
- Substitua o "X" pela informação correta;
- "X" deve começar em 1 na 1º exibição da frase e em 10 na última;
- Exiba a frase 10x no console.

"Esta é a Xº vez que esta frase é exibida no console."
*/

for (let phrase = 0; phrase <= 10; phrase++) {
    console.log(`Esta é a ${phrase}º vez que esta frase é exibida no console. `);
}